namespace WorkingWithPolynomials
{
    /// <summary>
    /// A class intended for storing result of polynomials division.
    /// </summary>
    public class PolynomialDivisionResult
    {
        /// <summary>
        /// Gets or sets a quotient.
        /// </summary>
        public Polynomial Quotient { get; set; }

        /// <summary>
        /// Gets or sets a remainder.
        /// </summary>
        public Polynomial Remainder { get; set; }
    }
}
